function buildReimbursementPromptV3(text) {
  return [
    {
      role: "system",
      content: REIMBURSEMENT_EXTRACTION_PROMPT_V3
    },
    {
      role: "user",
      content: `输入：\n${text}`
    }
  ];
}

function buildGeminiReimbursementPromptV3(text) {
  return [
    REIMBURSEMENT_EXTRACTION_PROMPT_V3,
    "",
    "## 用户输入",
    text
  ].join("\n");
}

